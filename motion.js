function fadeAway() {
    var x = document.getElementById("left-div");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  };

function fadeAwayRight() {
    var x = document.getElementById("right-div");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  };